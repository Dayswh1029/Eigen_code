#include <Eigen/Dense>
#include <iostream>
using namespace Eigen;

int main() {
    // ===== 输入 =====
    double k1 = 1, k2 = 2, k3 = 3;

    // 外力（4个节点）
    VectorXd f(4);
    f << 0, 1, 0, 0;  // 节点2施加力

    // ===== 第一步：建立A矩阵 =====
    // A(3×4)：3个弹簧，4个节点
    //         节点1  节点2  节点3  节点4
    MatrixXd A(3, 4);
    A << -1, 1, 0, 0,   // 弹簧1
        0, -1, 1, 0,   // 弹簧2
        0, 0, -1, 1;   // 弹簧3

    // ===== 第二步：建立C矩阵 =====
    // C(3×3)：对角线是弹簧刚度
    MatrixXd C = MatrixXd::Zero(3, 3);
    C(0, 0) = k1;
    C(1, 1) = k2;
    C(2, 2) = k3;

    // ===== 第三步：组装K =====
    // K = AᵀCA (4×4)
    MatrixXd K = A.transpose() * C * A;
    std::cout << "完整K矩阵：\n" << K << "\n\n";

    // ===== 第四步：施加BC =====
    // 节点1和节点4固定（大数法）
    double big = 1e30;
    K(0, 0) += big;
    K(3, 3) += big; // c++  从 0 开始 ！ 
    f(0) = 0;
    f(3) = 0;

    // ===== 第五步：求解 =====
    VectorXd u = K.ldlt().solve(f);
    std::cout << "位移u：\n" << u << "\n\n";

    // ===== 输出 =====
    // 计算每个弹簧的伸长量
    VectorXd e = A * u;
    std::cout << "弹簧伸长量：\n" << e << "\n\n";

    // 计算内力
    VectorXd internal_force = C * e;
    std::cout << "内力：\n" << internal_force << "\n";

    return 0;
}
//## 输出结果
